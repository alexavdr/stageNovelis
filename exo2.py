"""
Exercise 2. Write a program to iterate the first 10 numbers, and in each iteration,
print the sum of the current and previous number.

"""


def sum_nums():
    """
    iterates through numbers 1 to 10 and prints the sum of the current number and previous number
    :return:
    """
    for i in range(1, 11):
        print(2*i-1)


if __name__ == "__main__":
    sum_nums()
